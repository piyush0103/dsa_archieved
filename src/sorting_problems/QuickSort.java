	private static int[] sort(int low, int[] arr, int high) {
		// TODO Auto-generated method stub
		if (low < high) {
			int pivot = partition(low, arr, high);

			sort(low, arr, pivot - 1);
			sort(pivot + 1, arr, high);
		}
		return arr;
	}

	private static int partition(int low, int[] arr, int high) {
		// TODO Auto-generated method stub

		int pivot = arr[high];
		int j = low - 1;
		int i = 0;
		for (i = low; i <= high - 1; i++) {
			if (arr[i] < pivot) {
				j++;
				swap(j, i, arr);
			}
		}
		swap(j + 1, high, arr);

//		for (int a : arr) {
//			System.out.print(a + " ");
//		}
//		System.out.println();

		return j + 1;
	}

